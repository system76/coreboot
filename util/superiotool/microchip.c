static void enter_conf_mode_microchip(uint16_t port)
{
	OUTB(0x55, port);
}

static void exit_conf_mode_microchip(uint16_t port)
{
	OUTB(0xaa, port);
}

void probe_idregs_microchip(uint16_t port)
{
	probing_for("Microchip", "", port);

	enter_conf_mode_microchip(port);

	uint8_t legacy_id = regval(port, DEVICE_ID_REG_LEGACY);
	if (legacy_id != 0xfe) {
		if (verbose)
			printf(NOTFOUND "legacy_id=0x%02x, expected 0xfe\n", legacy_id);
		exit_conf_mode_microchip(port);
		return;
	}

	uint16_t id = (regval(port, DEVICE_ID_MSB) << 8) | regval(port, DEVICE_ID_LSB);
	uint8_t subid = regval(port, DEVICE_SUBID);
	uint8_t revision = regval(port, DEVICE_REVISION);

	uint16_t id_for_reg_table = id;
	uint32_t id_subid = (id << 8) | subid;
	// MEC142x parts use different numbers despite same family.
	if (id_subid == 0x001310 || id_subid == 0x001510)
		id_for_reg_table = 0x0011; // cheat a bit, they're compatible

	if (superio_unknown(reg_table, id_for_reg_table)) {
		if (verbose)
			printf(NOTFOUND "id=0x%04x, subid=0x%02x, rev=0x%02x\n", id, subid, revision);
		exit_conf_mode_microchip(port);
		return;
	}

	printf("Found %s %s (id=0x%04x, subid=0x%02x, rev=0x%02x) at 0x%x\n",
	       get_superio_name(reg_table, id_for_reg_table), lookup_chip_name(id, subid),
	       id, subid, revision, port);
	chip_found = 1;

	dump_superio("Microchip", reg_table, port, id_for_reg_table, LDN_SEL);

	exit_conf_mode_microchip(port);
}

void print_microchip_chips(void)
{
	print_vendor_chips("Microchip", reg_table);
}
