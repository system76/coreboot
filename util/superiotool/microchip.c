static const char *lookup_chip_name(uint16_t device_id, uint8_t subdevice_id)
{
	const uint32_t id = (((uint32_t)device_id) << 8) | subdevice_id;
	for (size_t i = 0; chip_names[i].devid_subdevid != EOT; i++) {
		if (chip_names[i].devid_subdevid == id)
			return chip_names[i].name;
	}
	return "UNKNOWN";
}

